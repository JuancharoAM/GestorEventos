    private void BtnComprarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnComprarActionPerformed
        int i = table_Eventos.getSelectedRow();
        int entradaDisponible = Integer.parseInt(table_Eventos.getValueAt(i, 4).toString());
        if (entradaDisponible == 0) {
            JOptionPane.showMessageDialog(null, "No hay tickets disponibles");

        } else {
            FrmCompraTickets ctm = new FrmCompraTickets(tipoEvento);
            ctm.setVisible(true);

            ctm.nombreEvento =(table_Eventos.getValueAt(i, 0).toString());
            ctm.LblNombreEvento.setText(ctm.nombreEvento);
            ctm.LblFechaEvento.setText(table_Eventos.getValueAt(i, 1).toString());
            ctm.LblHora.setText(table_Eventos.getValueAt(i, 2).toString());
            ctm.generaBotonesBloque();
            ctm.activaListenerBloque();
            this.setVisible(false);

            dispose();
        }
    }//GEN-LAST:event_BtnComprarActionPerformed

