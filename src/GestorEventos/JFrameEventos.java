    private void actualizarTabla() {
        List<Evento> eventosFiltrados = filtradoLista();

        if (eventosFiltrados != null && !eventosFiltrados.isEmpty()) {

            DefaultTableModel model = (DefaultTableModel) this.table_Eventos.getModel();
            model.setNumRows(0);

            for (Evento event : eventosFiltrados) {
                Object[] fila = new Object[5];
                fila[0] = event.getNombre();
                fila[1] = EventoTXT.convertirFechaTipoDate(event.getFecha());
                fila[2] = event.getHora();
                fila[3] = event.getTotalEntradas();
                fila[4] = event.getEntradasDisponibles();

                model.addRow(fila);
            }

        }
    }

    public List filtradoLista() {
        List<Evento> eventosFilter = eventos.stream().filter(item -> {

            return item.getTipoEvento().equals(tipoEvento);

        }).sorted(Comparator.comparing(Evento::getFecha).reversed()).collect(Collectors.toList());

        return eventosFilter;
    }

    public String cambioLabel() {
        String labelDefault = "/img/Fondo Otros Eventos.png";

        HashMap<String, String> hashLabel = new HashMap<>();

        hashLabel.put("Teatrales", "/img/Fondo Teatral.png");
        hashLabel.put("Comedias", "/img/Fondo Comedia.png");
        hashLabel.put("Musicales", "/img/Fondo Musical.png");
        hashLabel.put("Deportivos", "/img/Fondo Deportivo.png");

        return hashLabel.getOrDefault(tipoEvento, labelDefault);
    }

