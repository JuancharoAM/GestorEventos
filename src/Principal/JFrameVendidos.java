    private void actualizarTablaVendidos() {

        if (pilavendidos != null && !pilavendidos.isEmpty()) {

            DefaultTableModel model = (DefaultTableModel) this.tableVendidos.getModel();
            model.setNumRows(0);

            for (TicketComprado event : pilavendidos) {
                Object[] fila = new Object[5];
                fila[0] = event.getNombre();
                fila[1] = event.getFecha();
                fila[2] = event.getHora();
                fila[3] = event.getBloque();
                fila[4] = event.getNumero();

                model.addRow(fila);
            }

        }
    }
   
