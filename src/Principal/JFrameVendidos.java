    public List filtradoPila() {
        List<TicketComprado> vendidosFilter = pilavendidos.stream().filter(item -> {

            return "Teatrales".equals(lbl_Evento.getText());

        }).collect(Collectors.toList());

        return vendidosFilter;
    }

